package 运算操作;

public class ArithmeticoperatorDemo2 {
    public static void main(String[] args){
        //除法 /
        System.out.println(10/2);
        System.out.println(10/3);//整数 -- 得到小数
        System.out.println(10.0/3);//小数 -- 3.3333333333333335
        //取模
        System.out.println(10%2);
        System.out.println(10%3);
    }
}
